package array;

public class Multi_Type_Data {

	public static void main(String[] args) {
		// By using Multi type Data in an array
		
		
		Object ashik[]=new Object[5];
		
		ashik[0]="My ";
		ashik[1]="Number ";
		ashik[2]= 98;
		ashik[3]= " Means ";
		ashik[4]='A';
		
	System.out.println(ashik.length);
	

	
	for(int a=0;a<ashik.length;a++) {
		System.out.println(ashik[a]);
	}
		

	}

}
