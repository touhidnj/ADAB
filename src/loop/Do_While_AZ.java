package loop;

public class Do_While_AZ {

	public static void main(String[] args) {

		char i='a';
		
		do {
			System.out.print(i+" ");
			i++;
			
		}while (i<='z');
		
		System.out.println(".............");
		
		char n='Z';
		do {
			System.out.print(n+ "\" \" ");
			n--;
		}while (n>='A');
		
	}

}
