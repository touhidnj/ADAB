public class If_else_N_P {

	public static void main(String[] args) {
 // Find Negative & Positive Number
		
		double number = 8.1;
		
		if (number>0.0) {
			System.out.println("Number is positive");
		} else if (number<0.0) {
			System.out.println("Number is Negative");
		} else {
			System.out.println("Number is zero");
		}
		
		
	}

}
